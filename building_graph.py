import networkx as nx
import matplotlib.pyplot as plt

# Створюємо граф
G = nx.DiGraph()

# 1. Додаємо ребра з пропускною здатністю:
weighted_edges = [
    # Від Терміналів до Складів
    ("Термінал 1", "Склад 1", 25),
    ("Термінал 1", "Склад 2", 20),
    ("Термінал 1", "Склад 3", 15),
    ("Термінал 2", "Склад 3", 15),
    ("Термінал 2", "Склад 4", 30),
    ("Термінал 2", "Склад 2", 10),
    
    # Від Складу 1 до Магазинів
    ("Склад 1", "Магазин 1", 15),
    ("Склад 1", "Магазин 2", 10),
    ("Склад 1", "Магазин 3", 20),
    
    # Від Складу 2 до Магазинів
    ("Склад 2", "Магазин 4", 15),
    ("Склад 2", "Магазин 5", 10),
    ("Склад 2", "Магазин 6", 25),
    
    # Від Складу 3 до Магазинів
    ("Склад 3", "Магазин 7", 20),
    ("Склад 3", "Магазин 8", 15),
    ("Склад 3", "Магазин 9", 10),
    
    # Від Складу 4 до Магазинів
    ("Склад 4", "Магазин 10", 20),
    ("Склад 4", "Магазин 11", 10),
    ("Склад 4", "Магазин 12", 15),
    ("Склад 4", "Магазин 13", 5),
    ("Склад 4", "Магазин 14", 10)
]

# Додаємо всі ребра до графа
G.add_weighted_edges_from(weighted_edges)

# 2. Позиції для малювання графа
pos = {
    "Термінал 1": (2.0, 3.0),
    "Термінал 2": (7.0, 3.0),
    
    "Склад 1": (3.2, 5.0),
    "Склад 2": (5.6, 5.0),
    "Склад 3": (3.2, 1.0),
    "Склад 4": (5.6, 1.0),
    
    "Магазин 1": (0.8, 7.0),
    "Магазин 2": (2.0, 7.0),
    "Магазин 3": (3.2, 7.0),
    "Магазин 4": (4.4, 7.0),
    "Магазин 5": (5.6, 7.0),
    "Магазин 6": (6.8, 7.0),
    
    "Магазин 7": (0.8, -1.0),
    "Магазин 8": (2.0, -1.0),
    "Магазин 9": (3.2, -1.0),
    "Магазин 10": (4.4, -1.0),
    "Магазин 11": (5.6, -1.0),
    "Магазин 12": (6.8, -1.0),
    "Магазин 13": (8.0, -1.0),
    "Магазин 14": (9.2, -1.0)
}

# 3. Малюємо граф (Отут ШІшка трошки модернізувала код взятий із теоретичної частини, шоб все було красиво)
plt.figure(figsize=(16, 11))
plt.title("Логістична мережа з пропускною здатністю ребер", fontsize=14, pad=20)

nx.draw(
    G, pos,
    with_labels=True,
    node_size=3200,
    node_color="skyblue",
    font_size=9,
    font_weight="bold",
    arrows=True,
    arrowsize=18,
    connectionstyle="arc3"
)

edge_labels = nx.get_edge_attributes(G, 'weight')
nx.draw_networkx_edge_labels(
    G, pos, 
    edge_labels=edge_labels, 
    font_size=9, 
    font_color="black",
    label_pos=0.6
)

plt.axis('off')
plt.tight_layout()

# 4. Відображаємо граф
plt.show()